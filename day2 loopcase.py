# LOOP CASE

# 1.While Loop
# print 1 to 100 number while loop
i=0
while i<=10:
    print(i)
    i=i+1
print("End the Problem1")

# 2.FOR LOOP
# print 1 to 50 numbers in for loop
for i in range(31):
    print(i)
print("End the Problem2")

# 3.FOR LOOP
# Print range in between
for i in range(10,51,5): #  indicates the numbers in 10:starting value, 51:ending value, 5:increment value
    print(i)
print("End the Problem3")
