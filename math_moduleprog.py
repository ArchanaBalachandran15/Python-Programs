# Math Module Program

# In-built functions in Python
x = min(1, 2, 3, 4, 5, 6, 7, 8, 9)     # To print the minimum number from the given values
y = max(10, 202, 30, 40, 50, 60, 70)   # To print the maximum number from the given values
print("Minimum value:", x)
print("Maximum value:", y)

a = abs(-8)    # Converts negative number to positive (absolute value)
print("Absolute value of -8:", a)

power = pow(4, 3)   # Calculates 4 raised to the power 3 (4^3)
print("4 to the power of 3:", power)

# Importing math module to use its functions
import math

z = math.sqrt(64)    # Finds square root of 64
print("Square root of 64:", z)

c = math.ceil(1.5)   # Rounds number up to nearest integer
print("Ceiling of 1.5:", c)

f = math.floor(2.5)  # Rounds number down to nearest integer
print("Floor of 2.5:", f)

con = math.pi        # Constant value of Ï€ (pi)
print("Value of pi:", con)
