# if case condition

num=int(input("Enter a Number: "))
if num<0:
    print("Negative Number")
else:
    print("Positive Number")

# elif case condition

num=int(input("Enter a Number: "))
if num>0:
    print('Enter number is positive Number')
elif num==0:
    print("Enter number is zero")
else:
    print("Enter number is Negative Number")