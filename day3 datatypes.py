# string data type : IM-MUTABLE
str=('Apple','Mango','Grapes','Orange','Cherry')
print(str)        # print the string elements
print(len(str))   # length of the string
print(str[4:8])   # position of the string
print(str[-1])    # last position of the string

# List data type : MUTABLE DATA TYPE
list1=['Car','Bus','Van','Bike']
print(list1)       # print the list elements
print(len(list1))  # length of the list
list2=['Jeep','Scooter','Torus']
values=list1+list2 # added two list element 
print(values)      # print added list element

# Tuple data type : MUTABLE DATA TYPE
tuple1=("hello",'haii','world')
print(tuple1)

# Dictionary data type : Keyvalue pair
dict1={'name':'achu',"place":'kuvld',}
print(dict1)